				<p class="mt-2 mx-auto"> 
                
         <font color="#000000" size="3px" >
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The pilgrimage season in Sabarimala commences  from November 14 and extends to January 19 till Makara Vilakku. During this  period, millions of Ayyappa aspirants converge on the tiny temple complex from  all over India. Also the temple is open for brief spells during certain  Malayali festivals like Onam and Vishu. All through the year, monthly poojas  are offered at Sabarimala, usually during the first of week of every Malayalam  month (which actually falls in the middle of each English month); the shrine is  open for the first five days of every month.</font></p>
<p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Girls who have not yet attained puberty and  elderly women who have reached menopause are allowed entry into the temple. Men  are expected to walk barefoot, sleep on the floor with their hair and nails  uncut and refrain from self-indulgence during their 41-day vritham and journey  to Sabarimala.</font></p>
        <p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>Makara Vilakku</strong>&nbsp;
The most important festival at the Ayyapppa  temple on Sabarimala is Makara Vilakku. It is a seven-day festival, beginning  on the day of Makara Sankranthi, the day when the sun is in summer solstice.  According to legend, the idol of Dharma Shastha was enshrined in the temple on  this day. The annual festivities of Makara Vilakku commemorate this sacred  event.</font></p>
<p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The jewellery to adorn the idol during the  celebrations is brought from Pandalam Palace in a ceremonial procession that  starts from Valiya Koyikkal Sastha Temple at Pandalam, three days prior to  Makara Sankranthi. The boxes containing the sacred jewels are borne by an  oracle; the procession reaches Sabaripeettam in the evening on Makara  Sankranthi and is led to the Sannidhanam to the accompaniment of lights and  music. Incidentally, a kite appears in the sky at this very moment and hovers  around the boxes, as if to safeguard the precious cache comprising a diamond diadem,  gold bracelets and necklaces embedded with precious gems, Lord's swords, silver  arrows and images of elephant, horse and leopard fashioned out of gold.</font></p>
        <p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Another highlight of this festival is the  appearance of Makarajyothi that leaves an indelible impression on the millions  who view it. The poojas and rituals associated with Makara Vilakku are  performed on the Manimandapam (sacred platform) adjacent to the Devi's shrine.  A picture depicting Lord Ayyappan on the back of a tiger is placed on the podium.</font></p>
        <p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Afterwards, Malikappurathamma is mounted on an  elephant's back and taken in a procession of torch bearers, drummers and  buglers to Pathinettampadi (18 holy steps). The procession stops abruptly as  the Vettavili (call for hunting) is given out and returns, circumambulating the  main temple. Makara Vilakku ends with the ritual called 'Guruthi', offering  made to appease the god and goddesses of the wilderness. None remains within  the temple and its precincts after the 'Guruthi'.</font></p>
        <p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Other important festivals celebrated at the  temple include Onam, Mandalapooja and Vishu Vilakku </font>.</p>
        
        
