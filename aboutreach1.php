               <h1 align="left">Reaching Sabarimala  </h1>
        <p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The Ayyappa temple on Sabarimala lies 184 km away from the international airport in the Kerala's capital city of Thiruvananthapuram (Trivandrum). Similarly, it is 214 km from Kochi (formerly Cochin) International Airport. The nearest railway stations to Sabarimala are at Kottayam and Chengannur.</font></p>
 <p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;During the pilgrimage seasons, devotees proceed to the temple through Chalakayam, or they hit the mountain trails that start from Erumeli and ascend the Karimala hills barefoot. The total distance covered is approximately 50 km.</font></p>
         <p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
Three main routes to Sabarimala currently exist and they are,
<br/>
I) Kottayam to Pampa (via) Erumeli, from Pampa to Sabarimala (by foot) - 136 km Kottayam to Pampa (via) Manimala, from Manimala to Sabarimala by foot - 116 km
<br/>
II) Erumeli to Sabarimala (via) Kalaketty, Azhutha, Inchippara, Karimala, Pampa - 45 km From Pampa to Sabarimala by foot.
<br/>
III) Vandiperiyar to Mount Estate by vehicle, from Mount Estate to Sabarimala by foot.
<br/>
IV) Vandiperiyar to Kozhikkanam, from Kozhikkanam to Sabarimala (descent) - 15 km Kozhikkanam to Uppupura -10 km. Uppupura to Sabarimala - 3.5 km (Vehicles can be utilized for travel till Uppupura).</font></p>
 <p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Vehicles cannot proceed further beyond Pampa which is located in the Pampa river valley; the final five kilometers are best covered by trekking, but for the aged and handicapped, porters and chairs are available.
Click here for vehilce booking..
Email - mail@sabarimalaayyappan.com
<br/>
<br/>
Note:There aren't any fuel stations on Vadasserikkara-Pamba route. Fill your tanks and store enough quantities at Vadasserikkara. Though a fuel station is functioning at Pamba, availability of petrol and diesel cannot be ensured.
<br/>
<br/>
If your vehicle has any minor repairs kindly repair it before you enter the ghat section. You cannot find workshops at all places. There are many workshops at Erumely, Pathanamthitta, Vadasserikkara, etc.
<br/>
Keep important spare parts and tool kit with you. And don't forget that extra tyre.</font></p>

<p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
   <strong>Erumeli</strong> 
   <br/>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The vehicles bearing Ayyappas from various parts of the country are pooled at Erumeli. A temple with a presiding idol of Lord Ayyappan as an archer is located here. The idol subtly implies that the true seeker, guided by the Lord himself, shall concentrate on his goal of attaining God-realization. The way to Supreme bliss, however is not smooth, it is strewn with impediments. This is indicated by the cumbersome route beyond Erumeli.
<br/>
<br/>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;According to legend, it was at Erumeli that the young ascetic God, Dharma Shastha slew the demoness, Mahishi. There are two other small shrines adjacent to the main temple at Erumeli - one dedicated to Vavar who was the trusted aide of the Lord and another in honour of Ayyappan himself. Erumeli is synonymous with the famous Petta thullal that unfolds during the course of the pilgrimage. The devotees paint motifs on themselves with ashes, saffron and carbon and carry odd-looking arrows, clubs, bundles of fruits and vegetables. With Kanni Ayyappans lined up in the front, they dance rhythmically to the drum and cymbal beats and to the chants, 'Ayyappa thinthaka thom, Swami thinthaka thom.'
<br/>
<br/>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Petta thullal commences at the small Ayyappa shrine and proceeds to the main temple complex. As the dances reach the bigger temple, branches of trees are hurled at its roof and devotees circumambulate sanctum sanctorum. It is obligatory for Kanni Ayyappans to participate in Petta thullal. These rejoicings commemorate the joy of people after Mahishi was slain by Manikantan/Lord Ayyappan.</font></p>
<p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
<strong>Poomkavanam </strong>
<br/>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The area beyond Erumeli, extending to the banks of River Pampa is called Poomkavanam. It is an expansive stretch of lush impenetrable forest and the first spot of worship here, is a Shiva temple at Kalaketti. A coconut sanctified by the waters of Perur thodu is broken at the simple altar within the temple to propitiate Mahadeva.
<br/>
<br/>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Soon afterwards, the devotees reach Azhutha or River Alasa. While bathing in this river, every devotee picks up the first piece of stone he gets hold of from the river bed, which he throws on Kallidum kunnu where the mortal remains of Mahishi lied under a heap of stones. Kallidum kunnu is a mere rock, 4 feet high.
<br/>
<br/>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The trail then becomes extremely strenuous and hazardous. The steep slopes of Mount Azhutha pose a formidable challenge to pilgrims; Kallidum Kunnu grows visible as the summit of Azhutha draws nearer. This difficult climb culminates at a shrine for Kottayil Shastha on Inchipara where the worshippers break coconut and burn camphor to appease the presiding deity. At Inchipara, water is hardly available.
<br/>
<br/>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The next important stop on the way is the four feet high statue of Karimalanathan on Mount Karimala. Pilgrims pay homage to Karimalanathan by sprinkling turmeric power on the image and burning camphor in front of it. Also nearby is the Nazhikinar, a small well within a well that always contains fresh spring water. The very name Karimala suggests that it is the abode of wild elephants (Kari, in Malayalam means elephant). Ananthavalam lies beneath this formidable mountain and this is believed to the place where King Rajashekara picked up the infant Manikantan.</font></p>
<br/>
<p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
 <strong>Pampa</strong> 
<br/>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;From Karimala, the pilgrim group heads towards the banks of River Pampa. The cool and languid waters of Pampa are a welcome sight to hordes of travellers, weary after their arduous trek up Karimala. Long famed for its holiness, this river was brought to the earth by Parasurama with the blessings of Mahadeva to redeem his people; it is the confluence of all the holy rivers of Bharathavarsha. Upon reaching the banks of this hallowed river, the devotee rests and after a quiet dip in the sacred waters, offers Pithrutharpanam (rites to appease the ancestors), Pampa sadya (feast), Gurudakshina and Pampa vilakku (lamps).
<br/>
<br/>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The normally serene mountain valley turns into a surging ocean of humanity on festive occasions.</font></p>
<br/>
<p align="left">&nbsp;</p>
        <p align="left">&nbsp;</p>
        <p align="justify">&nbsp;
        <font color="#000000" size="3px" >
        <strong>Neelimala</strong>
        <br/> 
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The last lap of the journey is the ascent up Neelimala, atop which is the abode of the young ascetic God. The sacred 18 steps, Pathinettam padi leading to the Sannidhanam of Dharma Shastha are about 6 kilometres away from Pampa. Neelimala is higher and its slopes are steeper and slippery when compared to the surrounding hills.
<br/>
<br/>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;During Makara Vilakku, the representative of the King of Pandalam will be present on a platform at the base of this mountain with full royal insignia. Pilgrims honour him by presenting money (kanikka) and receive sacred vibhoothi as prasadam. The climb up Neelimala ends at Appachimedu. The trail hereafter meanders mostly on level ground.
<br/>
<br/>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Appachimedu is essentially a grassy meadow with dark and deep ravines on either side. These ravines named Appachikuzhi and Ippachikuzhi. Kanni Ayyappans are expected to fling sweetened rice balls into these chasms to appease evil spirits believed to be lurking in their shadowy depths. Appachimedu leads to Sabaripeettam which is just a flat rock. This is supposed to be the place where tapasvini Sabari who was an ardent devotee of Sri Ramachandra attained God realization through Dharma Shastha. As a mark of respect for this exalted soul, pilgrims break coconuts and burn camphor.
<br/>
<br/>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sabaripeettam is a mere 1.6 kilometres from the sacred 18 steps (pathinettam padi) Between these two spots lies Saramkuthi (or the place where the arrow struck) where Manikantan's arrow pierced a target to mark the location for the erection of the temple. Coconuts are broken here as well. Soon the 18 holy steps grow visible and the chants of Saranamantra become louder.
<br/>
<br/>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Before placing his foot on the step, the devotee prostrates before it and breaks a coconut upon it. The coconut is symbolic of the three gunas - Satwa, Rajas and Tamas. The outer shell represents Tamas, the kernel denotes Rajas and the nectar inside Satwa. When the pilgrim breaks the coconut, the shell and the kernel are broken into several pieces and the water spills over the step. This signifies a total destruction of Tamas and Rajas and the complete union of Satwa with the Ultimate Truth. And now the bhaktha is ready to savour Eternal Bliss.
<br/>
<br/>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The Lord with pattabandha which is a belt around the folded legs and chinmudra, the forefinger of the right hand touching the thumb and the stretched left arm resting on the knee cap welcomes the devout, poised to confer on them innumerable boons. The devotees circumambulate the temple and pay homage to Lord Ganesha and Karthikeya, while ghee is emptied out of their neithengas for abhishekam. A portion of the ghee is given back to them as Prasadam; the remainder is deposited in a large reservoir resembling a boat.
<br/>
<br/>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;After customary worships at the various shrines within the temple complex, the pilgrims descend the Pathinettampadi, contemplating a return in the next season.</font></p>  
                
                
                
                
                
                
                
                
                
                
